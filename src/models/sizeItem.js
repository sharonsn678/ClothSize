class SizeItem {
    constructor(
    id,
    regionIds,
    name,
    width,
    length,
    unit
    )
    {
        this.id = id;
        this.regionIds = regionIds;
        this.name = name;
        this.width = width;
        this.length = length;
        this.unit = unit
    }
}

export default SizeItem;